        <h2 class="card-title">CSV Statistical Reports</h2>
        <span style="font-size: 0.875rem; color: var(--text-muted);">
          Files generated by Statistical Jobs
        </span>
      </div>

      <div v-if="loading" style="text-align: center; padding: 2rem;">
        <i class="pi pi-spin pi-spinner" style="font-size: 2rem;"></i>
      </div>

      <div v-else-if="reports.length === 0" style="text-align: center; padding: 2rem; color: var(--text-muted);">
        <i class="pi pi-file" style="font-size: 3rem; margin-bottom: 1rem;"></i>
        <p>No reports generated yet</p>
      </div>

      <table v-else class="reports-table">
        <thead>
          <tr>
            <th>File Name</th>
            <th>Size</th>
            <th>Generated</th>
            <th>Actions</th>
          </tr>
        </thead>
        <tbody>
          <tr v-for="report in reports" :key="report.name">
            <td>
              <div style="display: flex; align-items: center; gap: 0.5rem;">
                <i :class="['pi', report.compressed ? 'pi-file-zip' : 'pi-file']"></i>
                {{ report.name }}
              </div>
            </td>
            <td>{{ formatSize(report.size_bytes) }}</td>
            <td>{{ formatDate(report.modified) }}</td>
            <td>
              <button class="btn btn-secondary" style="padding: 0.375rem 0.75rem;" @click="downloadReport(report)">
                <i class="pi pi-download"></i>
                Download
              </button>
            </td>
          </tr>
        </tbody>
      </table>
    </div>

    <!-- Export Tab -->
    <div v-if="activeTab === 'export'" class="card">
        <div class="card-header">
            <h2 class="card-title">Export Raw Data</h2>
            <span style="font-size: 0.875rem; color: var(--text-muted);">
                Download raw sensor data as CSV (Time-aligned)
            </span>
        </div>
        
        <form @submit.prevent="handleExport" class="export-form">
            <div class="form-row">
                <div class="form-group">
                    <label>Start Time</label>
                    <input type="datetime-local" v-model="exportForm.start_time" class="form-input" required max="2030-12-31T23:59">
                </div>
                <div class="form-group">
                    <label>End Time</label>
                    <input type="datetime-local" v-model="exportForm.end_time" class="form-input" required max="2030-12-31T23:59">
                </div>
            </div>

            <div class="form-group">
                <label>Select Sensors ({{ exportForm.sensor_ids.length }})</label>
                <div class="sensor-select-list">
                    <label v-for="sensor in sensorStore.sensors" :key="sensor.id" class="check-item">
                        <input type="checkbox" :value="sensor.id" v-model="exportForm.sensor_ids">
                        {{ sensor.name }}
                    </label>
                </div>
            </div>

            <div class="form-actions">
                <button type="submit" class="btn btn-primary" :disabled="exportLoading || exportForm.sensor_ids.length === 0">
                    <i v-if="exportLoading" class="pi pi-spin pi-spinner"></i>
                    <i v-else class="pi pi-download"></i>
                    Export CSV
                </button>
            </div>
        </form>
    </div>

  </div>
</template>

