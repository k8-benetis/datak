<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { api } from '../api'
import { useSensorStore } from '../stores/sensors'

interface Report {
  name: string
  path: string
  size_bytes: number
  modified: string
  compressed: boolean
}

const reports = ref<Report[]>([])
const loading = ref(false)
const sensorStore = useSensorStore()

// Tabs
const activeTab = ref<'jobs' | 'export'>('jobs')

// Export Form
const exportForm = ref({
    start_time: '',
    end_time: '',
    sensor_ids: [] as number[],
})
const exportLoading = ref(false)

onMounted(async () => {
  await Promise.all([
    fetchReports(),
    sensorStore.fetchSensors()
  ])
  
  // Set default dates (last 24 hours)
  const now = new Date()
  const yesterday = new Date(now.getTime() - 24 * 60 * 60 * 1000)
  // Format for datetime-local input: YYYY-MM-DDThh:mm
  exportForm.value.end_time = now.toISOString().slice(0, 16)
  exportForm.value.start_time = yesterday.toISOString().slice(0, 16)
})

async function fetchReports() {
  loading.value = true
  try {
    const response = await api.get('/api/config/reports')
    reports.value = response.data
  } catch (e) {
    console.error('Failed to fetch reports:', e)
  } finally {
    loading.value = false
  }
}

function formatSize(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`
}

function formatDate(dateStr: string): string {
  return new Date(dateStr).toLocaleString()
}

function downloadReport(report: Report) {
  window.open(`/api/config/reports/${report.name}`, '_blank')
}

async function handleExport() {
    exportLoading.value = true
    try {
        const payload = {
            sensor_ids: exportForm.value.sensor_ids,
            start_time: new Date(exportForm.value.start_time).toISOString(),
            end_time: new Date(exportForm.value.end_time).toISOString(),
            format: 'csv'
        }
        
        const response = await api.post('/api/export/raw', payload, {
            responseType: 'blob' // Important for file download
        })
        
        // Create download link
        const url = window.URL.createObjectURL(new Blob([response.data]))
        const link = document.createElement('a')
        link.href = url
        
        // Extract filename from header or default
        const contentDisposition = response.headers['content-disposition']
        let filename = 'export.csv'
        if (contentDisposition) {
             const filenameMatch = contentDisposition.match(/filename="?([^"]+)"?/)
             if (filenameMatch.length === 2)
                 filename = filenameMatch[1]
        }
        
        link.setAttribute('download', filename)
        document.body.appendChild(link)
        link.click()
        link.remove()
        
    } catch (e: any) {
        alert('Export failed: ' + (e.response?.data?.detail || e.message))
    } finally {
        exportLoading.value = false
    }
}
</script>

<template>
  <div>
    <div class="page-header">
      <h1 class="page-title">Reports & Data</h1>
      <div v-if="activeTab === 'jobs'">
        <button class="btn btn-secondary" @click="fetchReports">
            <i class="pi pi-refresh"></i> Refresh
        </button>
      </div>
    </div>

    <!-- Tabs -->
    <div class="tabs">
        <button 
